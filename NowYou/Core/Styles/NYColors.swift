import UIKit

public struct NYColors {
    
    public static func NYGreen() -> UIColor {
        return UIColor(hexValue: 0x60DF76)
    }
    
    public static func NYBorderColor() -> UIColor {
        return UIColor(hexValue: 0x979797)
    }
    
    public static func NYOrange() -> UIColor {
        return UIColor(hexValue: 0xFBAE5D)
    }
    
    public static func NYYellow() -> UIColor {
        return UIColor(hexValue: 0xF0CF3F)
    }
    
    public static func NYPink() -> UIColor {
        return UIColor(hexValue: 0xE58DD2)
    }
    
    public static func NYPurple() -> UIColor {
        return UIColor(hexValue: 0x744AF2)
    }
    
    public static func NYRed() -> UIColor {
        return UIColor(hexValue: 0xE32A2A)
    }
    
    public static func NYBlue() -> UIColor {
        return UIColor(hexValue: 0x0099FF)
    }
    
    public static func NYPhoneNumberColor() -> UIColor {
        return UIColor(hexValue: 0xBF4B7C)
    }
}
